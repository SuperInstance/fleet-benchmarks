/**
 * fleet-benchmarks: Report Generation
 * Outputs benchmark results to JSON and Markdown formats.
 */

import type {
  BenchmarkSuiteResult,
  BenchmarkResultEntry,
  BenchmarkComparison,
  SuiteComparison,
  StatsSummary,
} from './types';
import { formatNs, formatNumber } from './statistics';

// ── JSON Report ──────────────────────────────────────────────────

export function writeJsonReport(result: BenchmarkSuiteResult): string {
  return JSON.stringify(result, null, 2);
}

export function writeComparisonJsonReport(comparison: SuiteComparison): string {
  return JSON.stringify(comparison, null, 2);
}

// ── Markdown Report ──────────────────────────────────────────────

export function writeMarkdownReport(result: BenchmarkSuiteResult): string {
  const lines: string[] = [];

  lines.push(`# 📊 Fleet Benchmarks: ${result.suiteName}`);
  lines.push('');
  lines.push(`> Run ID: \`${result.runId}\` | ${new Date(result.timestamp).toISOString()}`);
  if (result.gitSha) {
    lines.push(`> Git SHA: \`${result.gitSha}\``);
  }
  lines.push(`> Config: ${result.config.iterations} iterations`);
  lines.push('');
  lines.push('---');
  lines.push('');

  // Group by category
  const categories = new Map<string, BenchmarkResultEntry[]>();
  for (const bench of result.benchmarks) {
    const list = categories.get(bench.category) ?? [];
    list.push(bench);
    categories.set(bench.category, list);
  }

  for (const [category, benchmarks] of categories) {
    lines.push(`## ${category}`);
    lines.push('');
    lines.push('| Benchmark | Mean | Median | p95 | p99 | Stddev | Throughput |');
    lines.push('|---|---|---|---|---|---|---|');

    for (const b of benchmarks) {
      lines.push(
        `| **${b.name}** | ${formatNs(b.stats.mean)} | ${formatNs(b.stats.median)} | ${formatNs(b.stats.p95)} | ${formatNs(b.stats.p99)} | ${formatNs(b.stats.stddev)} | ${formatNumber(Math.round(b.stats.throughput))} ops/s |`
      );
    }
    lines.push('');
  }

  // Summary
  lines.push('## Summary');
  lines.push('');
  lines.push(`- **Total benchmarks**: ${result.benchmarks.length}`);
  lines.push(`- **Categories**: ${categories.size}`);
  const totalOps = result.benchmarks.reduce((a, b) => a + b.stats.successes, 0);
  const totalFails = result.benchmarks.reduce((a, b) => a + b.stats.failures, 0);
  lines.push(`- **Total iterations**: ${formatNumber(totalOps)} succeeded, ${formatNumber(totalFails)} failed`);
  lines.push('');
  lines.push('---');
  lines.push('*Generated by fleet-benchmarks © SuperInstance*');

  return lines.join('\n');
}

export function writeComparisonMarkdownReport(comparison: SuiteComparison): string {
  const lines: string[] = [];

  lines.push('# 📊 Fleet Benchmarks: Comparison Report');
  lines.push('');
  lines.push(`> Before: \`${comparison.beforeRunId}\` (${new Date(comparison.beforeTimestamp).toISOString()})`);
  lines.push(`> After: \`${comparison.afterRunId}\` (${new Date(comparison.afterTimestamp).toISOString()})`);
  lines.push('');
  lines.push(`**Summary**: ${comparison.summary.improvements} improvements, ${comparison.summary.regressions} regressions, ${comparison.summary.neutral} neutral out of ${comparison.summary.total} benchmarks`);
  lines.push('');
  lines.push('---');
  lines.push('');

  // Regressions first
  const regressions = comparison.comparisons.filter(c => c.regression);
  const improvements = comparison.comparisons.filter(c => !c.regression && c.deltas.mean.percent !== 0);
  const neutral = comparison.comparisons.filter(c => c.deltas.mean.percent === 0);

  if (regressions.length > 0) {
    lines.push('## ⚠️ Regressions');
    lines.push('');
    lines.push('| Benchmark | Mean Δ | Median Δ | p95 Δ | Throughput Δ |');
    lines.push('|---|---|---|---|---|');
    for (const c of regressions) {
      lines.push(formatComparisonRow(c));
    }
    lines.push('');
  }

  if (improvements.length > 0) {
    lines.push('## ✅ Improvements');
    lines.push('');
    lines.push('| Benchmark | Mean Δ | Median Δ | p95 Δ | Throughput Δ |');
    lines.push('|---|---|---|---|---|');
    for (const c of improvements) {
      lines.push(formatComparisonRow(c));
    }
    lines.push('');
  }

  if (neutral.length > 0) {
    lines.push('## ➖ Neutral');
    lines.push('');
    lines.push('| Benchmark | Mean Δ | Median Δ | p95 Δ | Throughput Δ |');
    lines.push('|---|---|---|---|---|');
    for (const c of neutral) {
      lines.push(formatComparisonRow(c));
    }
    lines.push('');
  }

  lines.push('---');
  lines.push('*Generated by fleet-benchmarks © SuperInstance*');

  return lines.join('\n');
}

function formatComparisonRow(c: BenchmarkComparison): string {
  const fmt = (val: number) => {
    const sign = val >= 0 ? '+' : '';
    return `${sign}${val.toFixed(1)}%`;
  };
  return `| **${c.before.name}** | ${fmt(c.deltas.mean.percent)} | ${fmt(c.deltas.median.percent)} | ${fmt(c.deltas.p95.percent)} | ${fmt(c.deltas.throughput.percent)} |`;
}

// ── Stats Table (for inline rendering) ───────────────────────────

export function formatStatsTable(entry: BenchmarkResultEntry): string {
  const s: StatsSummary = entry.stats;
  return [
    `Benchmark: ${entry.name}`,
    `  Category:    ${entry.category}`,
    `  Iterations:  ${s.successes}/${s.iterations} succeeded`,
    `  Mean:        ${formatNs(s.mean)}`,
    `  Median:      ${formatNs(s.median)}`,
    `  Stddev:      ${formatNs(s.stddev)} (${s.rse.toFixed(1)}% RSE)`,
    `  Min/Max:     ${formatNs(s.min)} / ${formatNs(s.max)}`,
    `  p95/p99:     ${formatNs(s.p95)} / ${formatNs(s.p99)}`,
    `  Throughput:  ${formatNumber(Math.round(s.throughput))} ops/s`,
  ].join('\n');
}
